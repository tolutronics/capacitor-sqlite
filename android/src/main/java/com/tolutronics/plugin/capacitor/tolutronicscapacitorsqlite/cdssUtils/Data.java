package com.tolutronics.plugin.capacitor.tolutronicscapacitorsqlite.cdssUtils;

public class Data {
    public Integer id;
    public String name;
    public String value;

    public Data() {
        this.id = null;
        this.name = null;
        this.value = null;
    }

    public Data(Integer id, String name, String value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }
}
